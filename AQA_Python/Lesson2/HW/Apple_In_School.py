# n школярів ділять k яблук порівну, залишок, що не ділиться, залишається в кошику. Скільки яблук дістанеться кожному школяру? Скільки яблук залишиться у кошику? n та k можна задати вручну. вивести 2 числа
n = int(input('Скільки школярів? '))
k = int(input('Скільки є яблук? '))

forEachN = k//n
remainingApples = k%n

print('Стільки яблук дістанеться кожному школяру: ' + str(forEachN))
print('А стільки залишиться в кошику: ' + str(remainingApples))

