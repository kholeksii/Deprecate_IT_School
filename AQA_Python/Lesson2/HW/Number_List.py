#Написати прогаму, яка виводить попереднє та наступне число від заданого Наприклад: задане число 567, наступне буде 568, попереднє 565
print("Введіть задане число:")

enteredNumber = int(input())

print(f'Попереднє число від заданого: {enteredNumber-1}')
print(f'Задане число: {enteredNumber}')
print(f'Наступне число після заданого: {enteredNumber+1}')